package com.tallshort.cefdriver;

public class CefDriverException extends RuntimeException {

	private static final long serialVersionUID = -2846205465936855343L;

	public CefDriverException() {
	}

	public CefDriverException(String arg0) {
		super(arg0);
	}

	public CefDriverException(Throwable arg0) {
		super(arg0);
	}

	public CefDriverException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
